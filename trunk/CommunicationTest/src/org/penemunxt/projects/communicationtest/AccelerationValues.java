package org.penemunxt.projects.communicationtest;

public class AccelerationValues {
	int X;
	int Y;
	int Z;

	public int getX() {
		return X;
	}
	public void setX(int x) {
		X = x;
	}
	public int getY() {
		return Y;
	}
	public void setY(int y) {
		Y = y;
	}
	public int getZ() {
		return Z;
	}
	public void setZ(int z) {
		Z = z;
	}
	public AccelerationValues(int x, int y, int z) {
		super();
		X = x;
		Y = y;
		Z = z;
	}
}
